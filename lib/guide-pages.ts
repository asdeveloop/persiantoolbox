export const guidePages: GuidePage[] = guideSeeds.map((guide) => ({
  slug: guide.slug,
  title: guide.title,
  summary: guide.summary,
  body: guide.sections.join('\n\n'),
  faq: guide.faq,
  internalLinks: guide.internalLinks,
}));

export function getGuideBySlug(slug: string): GuidePage | null {
  return guidePages.find((guide) => guide.slug === slug) ?? null;
}
